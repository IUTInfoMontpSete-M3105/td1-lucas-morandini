
import java.io.File;
import java.time.LocalDate;
import java.util.*;

/**
 * 
 */
public class Rendu {

    /**
     * Default constructor
     */
    public Rendu(File doc, LocalDate date, int points) {
        this.doc = doc;
        this.date = date;
        this.points = points;

    }

    /**
     * Attribute
     */
    private File doc;

    /**
     * Attribute
     */
    private LocalDate date;

    /**
     * Attribute
     */
    private int points;





}